a = float(input('Digite a altura de uma parede: '))
l = float(input('Digite a largura de uma parede: '))
ar = l * a
litro = ar / 2
print(f'Sabendo que a sua parede tem {a}m de altura, e {l}m de largura, a área da sua parede é de {ar}m².')
print(f'Para poder pintar a parede será necessário {litro} litros de tinta.')
